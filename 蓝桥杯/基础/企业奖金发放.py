n = float(input())
if n <= 100000:
    li = n*0.1
elif n < 200000:
    li = 10000+(n-100000)*0.075
elif n < 400000:
    li = 10000+7500+(n-200000)*0.05
elif n < 600000:
    li = 10000+7500+200000*0.05+(n-400000)*0.03
elif n < 1000000:
    li = 10000+7500+200000*(0.05+0.03)+(n-600000)*0.015
else:
    li = 10000+7500+200000*(0.05+0.03)+400000*0.015+(n-1000000)*0.01
print('{:.2f}'.format(li))
